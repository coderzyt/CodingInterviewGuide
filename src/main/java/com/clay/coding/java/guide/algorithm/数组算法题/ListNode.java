package com.clay.coding.java.guide.algorithm.数组算法题;

/**
 * @author coderclay
 */
public class ListNode {

    int val;

    ListNode next;

    ListNode() {

    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
